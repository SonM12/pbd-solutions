public class NumbersAndMath {
	public static void main( String[] args ) {
		// Printing statement
		System.out.println( "I will now count my chickens:" );
		// calculating the number of Hens
		System.out.println( "Hens " + ( 25 + 30 / 6 ) );
		// calculating the number of Roosters
		System.out.println( "Roosters " + ( 100 - 25 * 3 % 4 ) );
		// printing statement
		System.out.println( "Now I will count the eggs:" );
		// calculating the number of eggs
		System.out.println( 3 + 2 + 1 - 5 + 4 % 2 - 1.0 / 4.0 + 6 );
		// printing a statement 
		System.out.println( "Is it true that 3 + 2 < 5 - 6.75?" );
		// whether the expression is true
		System.out.println( 3 + 2 < 5 - 6.75 );
		// printing statement and calculation
		System.out.println( "What is 3 + 2? " + ( 3 + 2 ) );
		// printing statement and calculation
		System.out.println( "What is 5 - 7? " + ( 5 - 6.75 ) );
		// printing the statement
		System.out.println( "Oh, that's why it's false." );
		// printing the statement
		System.out.println( "How about some more." );
		// printing the statement and result
		System.out.println( "Is it greater? " + ( 5 > -1.75 ) );
		// printing the statement and result
		System.out.println( "Is it greater or equal? " + ( 5 >= -1.75 ) );
		// printing the statement and result
		System.out.println( "Is it less or equal? " + ( 5 <= -1.75 ) );
	}
}
